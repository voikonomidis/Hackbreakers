    private void okJButtonActionPerformed(java.awt.event.ActionEvent evt) {
        //Shows the entity's name.
        String entityName = this.entityNameJTextField.getText();
        //Reads the entity's attributes through the jTable.
        List<Attribute> attrList = new ArrayList();
        for (int i = 0; i < this.attributesJTable.getModel().getRowCount(); i++) {
            attrList.add(
                    new Attribute((String) this.attributesJTable.getModel().getValueAt(i, 0),
                            new DataType((String) this.attributesJTable.getModel().getValueAt(i, 1)))
            );
        }

        //Stores the new entity in the (.sys) file and adds it to db's jTree.
        Database db = new Database();
        if (db.createEntityGUI(this.databaseNameJTextField.getText(), entityName, attrList)) {
            //A message of successful storage is appeared.
            JOptionPane.showMessageDialog(this, "Succesfully created entity with name " + entityName + " !");
            //Updates the jTree.
            MainFrame mainFrm = (MainFrame) this.getParent();
            JScrollPane jScrollPane = mainFrm.getDbObjectsTreeJScrollPane();
            JTree entityTree = (JTree) jScrollPane.getViewport().getView();
            DefaultTreeModel model = (DefaultTreeModel) entityTree.getModel();
            DefaultMutableTreeNode root = (DefaultMutableTreeNode) entityTree.getModel().getRoot();
            DefaultMutableTreeNode child = new DefaultMutableTreeNode(entityName);
            model.insertNodeInto(child, root, root.getChildCount());
            entityTree.scrollPathToVisible(new TreePath(child.getPath()));
        } else {
            //Otherwise, an error message is appeared.
            JOptionPane.showMessageDialog(this, "Could not create entity with name " + entityName + "...",
                    "Entity exists or other error",
                    JOptionPane.ERROR_MESSAGE);
        }
        this.dispose();
    }

