        //Pulls the entities from the database.
        Database db = new Database();
        List<Entity> entList = db.fetchDatabaseEntitiesGUI(this.databaseNameJTextField.getText());
        //Adds new row on the jTable.
        TableColumn dataTypeColumn = this.attributesJTable.getColumnModel().getColumn(1);
        DefaultTableModel tableModel = (DefaultTableModel) this.attributesJTable.getModel();
        JComboBox comboBox = new JComboBox();
        comboBox.addItem("Numeric");
